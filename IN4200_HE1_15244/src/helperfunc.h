//=============================================================================
void printvec(int *v, int n)
//----------------------------------------------------------------------------
//
//----------------------------------------------------------------------------
{
	printf("| ");
	for (size_t i = 0; i < n; ++i) {
		printf("%d ", v[i]);
	}
	printf("|\n");
}
//=============================================================================


//=============================================================================
void printmat(char **A, int m, int n)
//----------------------------------------------------------------------------
//
//----------------------------------------------------------------------------
{
	for (size_t i = 0; i < m; ++i) {
		printf("| ");
		for (size_t j = 0; j < n; ++j) {
			printf("%d ", A[i][j]);
		}
		printf("|\n");
	}
}
